package es.tipolisto.breeds.data.model;

public class Image {
    private String id;
    private int width;
    private int height;
    private String url;

    public String getId() {
        return id;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public String getUrl() {
        return url;
    }
}
